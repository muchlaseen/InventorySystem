    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        
        try {
            String sql = "INSERT INTO mahasiswa VALUES ('" + txtNim.getText() + "','" + txtNama.getText() + "','" + cbJurusan.getSelectedItem() + "','" + txtAlamat.getText() + "','" + txtTelf.getText() + "')";
            Connection conn = (Connection)Config.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ups, data gagal disimpan");
        }
        loadTable();
        resetForm();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void tbMhsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMhsMouseClicked
        int row = tbMhs.rowAtPoint(evt.getPoint());
        String nim = tbMhs.getValueAt(row, 1).toString();
        txtNim.setText(nim);
        String nama= tbMhs.getValueAt(row, 2).toString();
        txtNama.setText(nama);
        String jurusan = tbMhs.getValueAt(row, 3).toString();
        cbJurusan.setSelectedItem(jurusan);
        String alamat = tbMhs.getValueAt(row, 4).toString();
        txtAlamat.setText(alamat);
        String telf = tbMhs.getValueAt(row, 5).toString();
        txtTelf.setText(telf);
    }//GEN-LAST:event_tbMhsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String sql = "UPDATE mahasiswa SET nim = '" + txtNim.getText() + "', nama = '" + txtNama.getText() + "', jurusan = '" + cbJurusan.getSelectedItem()+"', alamat = '" + txtAlamat.getText() + "', no_telf = '" + txtTelf.getText() + "' WHERE nim = '" + txtNim.getText() + "'";
            Connection conn = (Connection)Config.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Data gagal diubah, karena " + e.getMessage());
        }
        loadTable();
        resetForm();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            String sql = "DELETE FROM mahasiswa WHERE nim = '" + txtNim.getText() + "'";
            Connection conn = (Connection)Config.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, "Data gagal dihapus, karena " + e.getMessage());
        }
        loadTable();
        resetForm();
    }//GEN-LAST:event_btnDeleteActionPerformed

