    private void loadTable() {
        DefaultTableModel model= new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("NIM");
        model.addColumn("Nama");
        model.addColumn("Jurusan");
        model.addColumn("Alamat");
        model.addColumn("No. Telfon");
        
        // load dari database ke table model
        try {
            int no_tb = 1;
            String sql = "SELECT * FROM mahasiswa";
            Connection conn = (Connection)Config.configDB();
            Statement sttm = conn.createStatement();
            ResultSet rs = sttm.executeQuery(sql);
            while(rs.next()) {
                model.addRow(new Object[]{no_tb++, rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});    
            }
            tbMhs.setModel(model);
        } catch (Exception e) {
        }
    }
    
    private void resetForm() {
        txtNim.setText(null);
        txtNama.setText(null);
        cbJurusan.setSelectedIndex(0);
        txtAlamat.setText(null);
        txtTelf.setText(null);
        
    }
